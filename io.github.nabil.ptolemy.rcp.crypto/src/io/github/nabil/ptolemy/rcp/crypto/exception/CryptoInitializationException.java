package io.github.nabil.ptolemy.rcp.crypto.exception;

public class CryptoInitializationException extends Exception {

	private static final long serialVersionUID = 1L;

	public CryptoInitializationException() {
		super();
	}

	public CryptoInitializationException(String message, Throwable cause) {
		super(message, cause);
	}

	public CryptoInitializationException(String message) {
		super(message);
	}
}
